max_parallel=${MAX_PARALLEL_CHECKS:-20}
active_pids=()

run_check() {
  local package="$1"
  local buffer=""
  buffer+="checking $package\n"
  if out=$(yarn info -A -R "$package" 2>&1); then
    buffer+=$'\n'"$(printf '\033[1;33m[WARNING]\033[0m %s is present (yarn info succeeded).\n' "$package")"
    buffer+=$'\n'"$out"$'\n'
  else
    buffer+="$(printf '\033[1;90m[skip]\033[0m %s: yarn info failed (non-zero exit).\n' "$package")"$'\n'
  fi
  printf '%b' "$buffer"
}

for package in "${compromised[@]}"; do
  run_check "$package" &
  active_pids+=("$!")

  if ((${#active_pids[@]} >= max_parallel)); then
    wait "${active_pids[0]}"
    active_pids=("${active_pids[@]:1}")
  fi
done

for pid in "${active_pids[@]}"; do
  wait "$pid"
done
